-- SUPABASE MIGRATION SCHEMA FOR CLOVER
-- This file translates the local SQLite database into a Multi-Tenant PostgreSQL database.
-- It adds a `user_id` to every table so multiple users can have their own isolated CRM.
-- Instructions: Paste this entire script into the Supabase SQL Editor and hit "Run".

-- 1. Create Contacts Table
CREATE TABLE Contacts (
    Contact_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    Name TEXT NOT NULL,
    Email TEXT,
    Phone TEXT,
    Lead_Source TEXT
);
ALTER TABLE Contacts ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Users can manage their own Contacts" ON Contacts FOR ALL USING (auth.uid() = user_id);

-- 2. Create Inquiries Table
CREATE TABLE Inquiries (
    Inquiry_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    Contact_ID BIGINT NOT NULL REFERENCES Contacts(Contact_ID) ON DELETE CASCADE,
    Service_Type TEXT,
    Event_Date TEXT,
    Pipeline_Stage TEXT DEFAULT 'New Inquiry',
    Estimated_Value REAL DEFAULT 0.0
);
ALTER TABLE Inquiries ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Users can manage their own Inquiries" ON Inquiries FOR ALL USING (auth.uid() = user_id);

-- 3. Create Communications Table
